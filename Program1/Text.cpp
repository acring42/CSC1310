#include "Text.h"
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Text::Text(const char* text){
    textLength = getLength(text);
    char* textArrayTemp = new char[textLength+1];
    strcpy(textArrayTemp, text);
    textArray = textArrayTemp;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Text::~Text(){
    delete [] textArray;
    cout << "\nText Destructor: Released memory for textArray." << endl;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Text::displayText(){
    cout << textArray;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const char* Text::getText(){
    return textArray;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int Text::getLength(string a) const{
    return a.length();
}
